# Arithmetic operators
# +,-,*,/
a = 4
b= 5
c= a+b
d = a-b
e = a*b
f= a/b
print(c,d,e,f)
#Assignment Operators 
# =,+=,-=,*=,/=
g = 10
g +=10
print(g)
#Comparison Operators
# ==,!=,>,<,>=,<=
h = 5 == 4
i = 5 != 4
j = 5 > 4
k = 5 < 4
l = 5 >= 4
m = 5 <= 4
print(h,i,j,k,l,m)

#Logical Operators
# and, or, not

# and operator
print(False and False)
print(False and True)
print(True and False)
print(True and True)

# or operator
print(False or False)
print(False or True)
print(True or False)
print(True or True)

# not operator
print(not False)